''' AULA 5 - Operadores aritméticos
+ Adição
- Subtração
* Multiplicação
/ Divisão (valor real)
// Divisão (valor inteiro)
** exponenciação
% resto da divisão
'''
print('Adição:',10+10) # -> 20
print(10-3) # -> 7
print(10*3) # -> 30
print(10/3) # -> 3.3333333333333335
print(10//3) # -> 3
print(10**3) # -> 1000
print(10%3) # -> 0
